    private void fill_model() {
        try {

            Object[] o = null;
            List<Contrato> lista = new ContratoBean().readAll();
            int c = modelo.getRowCount();
            for (int i = 0; i < c; i++) {
                modelo.removeRow(0);
            }

            for (int i = 0; i < lista.size(); i++) {
                modelo.addRow(o);
                modelo.setValueAt(lista.get(i).getContId(), i, 0);
                modelo.setValueAt(lista.get(i).getContNomb(), i, 1);
                modelo.setValueAt(lista.get(i).getContProy(), i, 2);
                modelo.setValueAt(new uDate().dateTable(lista.get(i).getContFini()), i, 3);
                modelo.setValueAt(new uDate().dateTable(lista.get(i).getContFfin()), i, 4);

                modelo.setValueAt(lista.get(i).getContPlaz(), i, 5);
                modelo.setValueAt(lista.get(i).getContMont(), i, 6);
                modelo.setValueAt(lista.get(i).getContEspe(), i, 7);
                modelo.setValueAt(new uDate().dateTable(lista.get(i).getAudiFreg()), i, 8);

            }

        } catch (Exception e) {

            System.out.println("ERROR: " + e.getMessage());
        }
    }

    private void seleccionado() {
        int row = jTable1.getSelectedRow();
        txtNomb.setText(jTable1.getValueAt(row, 1).toString());
        txtProy.setText(jTable1.getValueAt(row, 2).toString());
        jdFini.setDate(new uDate().tableToDate(jTable1.getValueAt(row, 3).toString()));
        jdFfin.setDate(new uDate().tableToDate(jTable1.getValueAt(row, 4).toString()));
        txtPlaz.setText(jTable1.getValueAt(row, 5).toString());
        txtMonto.setText(jTable1.getValueAt(row, 6).toString());
        txtEspe.setText(jTable1.getValueAt(row, 7).toString());
        txtFreg.setText(jTable1.getValueAt(row, 8).toString());
    }

